# Python Basics

# Variables
name = "Alice"
age = 25
height = 5.6

print(f"Name: {name}")
print(f"Age: {age}")
print(f"Height: {height}")

# Data Types
integer_variable = 10  # Integer
float_variable = 10.5   # Float
string_variable = "Hello, World!"  # String
boolean_variable = True  # Boolean

print(f"Integer: {integer_variable}")
print(f"Float: {float_variable}")
print(f"String: {string_variable}")
print(f"Boolean: {boolean_variable}")

# Type Conversion
age_as_string = str(age)
height_as_integer = int(height)
numeric_string = "99.99"
converted_float = float(numeric_string)

print(f"Age as string: {age_as_string}")
print(f"Height as integer: {height_as_integer}")
print(f"Converted float: {converted_float}")
